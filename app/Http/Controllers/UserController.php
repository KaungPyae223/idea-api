    public function index()
    {
        $users = User::all()->map(function ($user) {
            return [
                "id" => $user->id,
                "name" => $user->name,
                "email" => $user->email,
                "roles" => $user->roles->pluck('role')->toArray(),
                "permissions" => $user->roles->flatMap->permissions->pluck('permission')->unique()->toArray()
            ];
        });

        return response()->json($users);
    }

